namespace Net.Belt.Comparisons;

/// <summary>
/// Represents the ordering direction for sorting operations.
/// </summary>
public enum Direction
{
	/// <summary>
	/// Specifies that the sorting operation should arrange elements in ascending order,
	/// where items are ordered from the smallest to the largest based on the sorting criteria provided.
	/// </summary>
	Ascending,

	/// <summary>
	/// Specifies that the sorting operation should arrange elements in descending order,
	/// where items are ordered from the largest to the smallest based on the sorting criteria provided.
	/// </summary>
	Descending
}